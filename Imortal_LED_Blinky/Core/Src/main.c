int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MPU Configuration--------------------------------------------------------*/
  MPU_Config();

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_TIM8_Init();
  /* USER CODE BEGIN 2 */


  /* USER CODE END 2 */



  /* Initialize USER push-button, will be used to trigger an interrupt each time it's pressed.*/
  BSP_PB_Init(BUTTON_USER, BUTTON_MODE_EXTI);

  /* Initialize COM1 port (115200, 8 bits (7-bit data + 1 stop bit), no parity */
  BspCOMInit.BaudRate   = 115200;
  BspCOMInit.WordLength = COM_WORDLENGTH_8B;
  BspCOMInit.StopBits   = COM_STOPBITS_1;
  BspCOMInit.Parity     = COM_PARITY_NONE;
  BspCOMInit.HwFlowCtl  = COM_HWCONTROL_NONE;
  if (BSP_COM_Init(COM1, &BspCOMInit) != BSP_ERROR_NONE)
  {
    Error_Handler();
  }

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  uint32_t pwmValue = 0;
  int8_t direction = 1;

  // Initialize all channels first
  HAL_TIM_PWM_Init(&htim8);
  HAL_TIMEx_PWMN_Start(&htim8, TIM_CHANNEL_2);   // Start complementary output
  __HAL_TIM_ENABLE(&htim8);
  __HAL_TIM_MOE_ENABLE(&htim8);

  // Optional: Try forcing the output state initially
  __HAL_TIM_SET_COMPARE(&htim8, TIM_CHANNEL_2, 500);  // Set to mid-brightness to test

  while (1) {
      pwmValue += direction;

      if (pwmValue >= 999 || pwmValue <= 0) {
          direction *= -1;
      }

      // Update PWM for Red and Green LEDs
      __HAL_TIM_SET_COMPARE(&htim8, TIM_CHANNEL_2, pwmValue);  // Controls both LEDs

      // Toggle Yellow LED with a different pattern
      if (pwmValue < 333) {
          HAL_GPIO_WritePin(GPIOE, GPIO_PIN_1, GPIO_PIN_SET);    // Yellow LED ON
      } else if (pwmValue < 666) {
          HAL_GPIO_WritePin(GPIOE, GPIO_PIN_1, GPIO_PIN_RESET);  // Yellow LED OFF
      } else {
          HAL_GPIO_WritePin(GPIOE, GPIO_PIN_1, GPIO_PIN_SET);    // Yellow LED ON
      }

      HAL_Delay(1);
  }
}
